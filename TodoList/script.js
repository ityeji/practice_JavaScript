const calculator = {
    add: function(a, b){
        console.log(a + b);
    },
    minus: function(a, b){
        console.log(a - b);
    },
    div: function(a, b){
        console.log(a / b);
    },
    powerof: function(a, b){
        console.log(a ** b);
    },
};

calculator.add(2, 5);
calculator.minus(2, 5);
calculator.div(2, 5);
calculator.powerof(2, 5);